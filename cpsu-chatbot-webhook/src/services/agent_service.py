# ==================== Custom State ====================
class AgentState(MessagesState):
    """Extended state that keeps routing decision separate from messages."""
    route: str


# ==================== Structured Router Output ====================
class RouteDecision(BaseModel):
    """Structured output for the router LLM."""
    agent: Literal["general_agent", "academic_agent", "finance_agent", "staff_agent"] = Field(
        description="The agent best suited to handle the user's query"
    )


